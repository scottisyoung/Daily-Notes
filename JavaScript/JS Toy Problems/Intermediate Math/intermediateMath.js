//  FOR LOOP INCREMENT COUNTER //
//  Taking number1 and incrementing it by numInc //

function myLoop(num1, numInc) {
    for (var i = 0; i < numInc; i++) {
        num1++;
    }
    return num1;
}
myLoop(3, 4); 
console.log(myLoop(2,2));